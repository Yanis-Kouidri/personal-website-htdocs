        <h4> Personal reflection </h4>
        <p class="block">
            We need to keep in mind that people are different, and we can’t force them to go into specific sectors. 
            For example, it is easy to see that jobs related to the technology or finance sectors are often held by men. On the other hand, jobs related to care or childhood are often done by women. 
            You may think that this is due to patriarchy or some other social construction, but the studies carried out on this subject do not support this view. 
            In fact, in the most advanced countries in the world in terms of gender equality, namely Norway, Sweden, and Denmark, we notice the biggest gender disparities, with women going into care and social professions much more than into technical or scientific professions. 
            These differences in orientation can therefore be explained by biological differences. This also explains the differences in salary between men and women. 
            To learn more about this subject, here is the <a href="https://www.bbc.com/worklife/article/20190831-the-paradox-of-working-in-the-worlds-most-equal-countries" target="_blank">link to the study</a>.
            However, it is important to keep in mind that we are talking about the average here, there are many very competent women in the fields of technology and science as well as men who are very involved in the social and care professions.
            The purpose of my reflection is to alert us to certain practices that are supposed to solve the problem of diversity, but whose concrete effect is not the one expected.

